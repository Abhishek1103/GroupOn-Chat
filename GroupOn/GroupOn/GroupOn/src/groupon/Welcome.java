    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        int flag=1;
        String paswrd=new String(txtPass.getPassword());
        String confPass=new String(txtConfPass.getPassword());
        String name=txtName.getText().trim();
        String username=txtUsnm.getText().trim();
        
      if(paswrd.length()<25 && name.length()<25 && username.length()<32)
      {
            
        if(paswrd.equals(confPass) && validateUsnm(username))
        {
            try
            {
                Socket sock=new Socket("172.31.78.171",5003);
                System.out.println("Connected to chat server...");
                Streams s= new Streams(sock);
                Streams.dout.writeInt(flag);
                Streams.dout.writeUTF(name);
                Streams.dout.writeUTF(username);
                Streams.dout.writeUTF(paswrd);
                boolean valid=Streams.dis.readBoolean();
                System.out.println("Recieved valiated="+valid);
              
              if(valid)
              {
                  new File(System.getProperty("user.home")+"/GroupOn/"+username).mkdirs();
                  PrintWriter pw=new PrintWriter(new BufferedWriter(new FileWriter(System.getProperty("user.home")+"/GroupOn/"+username+"/credentialsLog.txt", true)));
                  pw.println(username);
                  pw.println(name);
                  pw.close();
                  
                  new FileWriter(System.getProperty("user.home")+"/GroupOn/allUserLog.txt");
                    
                  UtilityMethods.loginStatusLog(username, 1);
                    
                  System.out.println("AlluserLog saved Successfully");
                  System.out.println("SignUp Successful");
                  Messenger.isOnline=true;
                  UploadPic upObj = new UploadPic(sock,username);
                  upObj.setVisible(true);
                  this.dispose();
              }
              else
              {
                  lblUsername.setText("Username Unavailable");      
                  lblUsername.setForeground(new java.awt.Color(222, 41, 41));
                  lblUsername.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(233, 22, 22), 1, true));
              }
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
        else
        {
            txtConfPass.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(233, 22, 22), 1, true));
            JOptionPane.showMessageDialog(null, "Passwords Do Not Match");
        }
     }
      else
      {
          if(paswrd.length()>24)
              JOptionPane.showMessageDialog(null, "Max length of Password is 24");
          if(name.length()>24)
              JOptionPane.showMessageDialog(null, "Name Should Not Exceed 24 characters");
          if(username.length()>31)
              JOptionPane.showMessageDialog(null, "Username Should not exceed 31 Charaters");
      }
    }//GEN-LAST:event_btnSignUpActionPerformed

