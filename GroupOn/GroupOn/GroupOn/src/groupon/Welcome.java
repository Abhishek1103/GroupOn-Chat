    int flag=0;
        String username=txtUsrnm.getText().trim();
        String pass=new String(passFld.getPassword());
     
        try
        {   
            
            Socket sock=new Socket("172.31.78.171",5003);
            System.out.println("Connected to chat server...");
            
            Streams s= new Streams(sock);
            
            Streams.dout.writeInt(flag);
            Streams.dout.writeUTF(username);
            Streams.dout.writeUTF(pass);
            System.out.println("Sent flag, username, password");
            boolean validated=Streams.dis.readBoolean();
            System.out.println("Recieved valiated="+validated);
            if(validated)
            {

                
                System.out.println("Login Successful");
                UtilityMethods.loginStatusLog(username, 1);
                System.out.println("UtilityMethod completed");
                 
                System.out.println("Recieve Started");
                Messenger.isOnline=true;
                Messenger msg =new Messenger(sock,username);
                msg.setVisible(true);
                System.out.println("Opening Messenger");
                this.dispose();              
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Invalid Credentials");
            }
           
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    
    
