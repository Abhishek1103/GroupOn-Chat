    boolean validateUsnm(String userName){
        boolean validation=false;
        try 
        {
            
            if(userName.isEmpty() || userName==""  )
            {
                validation=false;
            }
            else
            {
                validation=true;
            }
            
        }
        catch (Exception ex)
        {
            ex.printStackTrace();
        }
        
        return validation;
    }
    
